#OPerações
x = 10
y = 2

#Adicao
a = x + y
#Sub
b = x - y
#Multiplica
c = x * y
#Divisão
d = x / y
#Modulo
e = x % y

#Sem variavel
print('\nSoma: {}'.format(x + y))
print('Sub: {}'.format(x - y))
print('Mult: {}'.format(x * y))
print('Div: {}'.format(x / y))
print('Modulo: {}'.format(x % y))

#Com variavel
print('\nSoma: {}'.format(a))
print('Sub: {}'.format(b))
print('Mult: {}'.format(c))
print('Div: {}'.format(d))
print('Modulo: {}\n'.format(e))
